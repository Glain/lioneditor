        private static Dictionary<int, string> fftPackFiles;

        public static Dictionary<int, string> FFTPackFiles
        {
            get
            {
                if ( fftPackFiles == null )
                {
                    fftPackFiles = new Dictionary<int, string>();

                    XmlDocument doc = new XmlDocument();
                    doc.LoadXml( PatcherLib.Properties.Resources.FFTPackFiles );

                    XmlNodeList nodes = doc.SelectNodes( "/files/file" );
                    foreach ( XmlNode node in nodes )
                    {
                        fftPackFiles.Add( Convert.ToInt32( node.Attributes["entry"].InnerText ), node.Attributes["name"].InnerText );
                    }
                }

                return fftPackFiles;
            }
        }


        public static void DumpToDirectory( string filename, string path, BackgroundWorker worker )
        {
            FileStream stream = null;

            try
            {
                stream = new FileStream( filename, FileMode.Open, FileAccess.Read );
                DumpToDirectory( stream, path, worker );
            }
            catch( Exception )
            {
                throw;
            }
            finally
            {
                if( stream != null )
                {
                    stream.Close();
                }
            }
        }

        public static void DumpToDirectory( FileStream stream, string path, BackgroundWorker worker )
        {
            MakeDirectories( path, "BATTLE", "EFFECT", "EVENT", "MAP", "MENU", "SOUND", "WORLD", "SAVEIMAGE", "unknown", "OPEN" );
            for( int i = 1; i <= 3970; i++ )
            {
                byte[] bytes = GetFile( stream, i );
                string filename = string.Empty;

                if( FFTPackFiles.ContainsKey( i ) )
                {
                    filename = FFTPackFiles[i];
                }
                else if( bytes.Length == 0 )
                {
                    filename = string.Format( "unknown/fftpack.{0}.dummy", i );
                }
                else
                {
                    filename = string.Format( "unknown/fftpack.{0}", i );
                }

                worker.ReportProgress( ( i * 100 ) / 3790, string.Format( "Extracting {0}", filename ) );

                filename = Path.Combine( path, filename );

                SaveToFile( bytes, filename );

            }
        }

        public static byte[] GetFile( Stream stream, int index )
        {
            byte[] bytes = new byte[4];
            stream.Seek( index * 4 + 4, SeekOrigin.Begin );
            stream.Read( bytes, 0, 4 );
            UInt32 start = bytes.ToUInt32();

            UInt32 end;
            if( index == 3970 )
            {
                end = (UInt32)stream.Length;
            }
            else
            {
                stream.Read( bytes, 0, 4 );
                end = bytes.ToUInt32();
            }

            UInt32 length = end - start;

            byte[] result = new byte[length];

            stream.Seek( start, SeekOrigin.Begin );
            stream.Read( result, 0, (int)length );

            return result;
        }

        public static byte[] GetFileFromIso( Stream stream, Files file )
        {
            stream.Seek( (int)PspIso.Files.PSP_GAME.USRDIR.fftpack_bin * 2048, SeekOrigin.Begin );
            return GetFileFromFFTPack( stream, file );
        }

        public static byte[] GetFileFromFFTPack( Stream stream, Files file )
        {
            long pos = stream.Position;
            byte[] loc = new byte[4];

            int index = (int)file;
            stream.Seek( index * 4 + 4 + pos, SeekOrigin.Begin );
            stream.Read( loc, 0, 4 );
            UInt32 start = loc.ToUInt32();

            UInt32 end;
            if ( index >= 3970 )
            {
                throw new ArgumentException( "file" );
            }
            else
            {
                stream.Read( loc, 0, 4 );
                end = loc.ToUInt32();
            }

            UInt32 length = end - start;
            byte[] result = new byte[length];
            stream.Seek( start + pos, SeekOrigin.Begin );
            stream.Read( result, 0, (int)length );

            return result;
        }

        public static void MergeDumpedFiles( string path, string filename, BackgroundWorker worker )
        {
            FileStream stream = null;
            try
            {
                stream = new FileStream( filename, FileMode.Create );
                stream.WriteByte( 0x80 );
                stream.WriteByte( 0x0F );
                stream.WriteByte( 0x00 );
                stream.WriteByte( 0x00 );
                stream.WriteByte( 0x10 );
                stream.WriteByte( 0x00 );
                stream.WriteByte( 0x00 );
                stream.WriteByte( 0x00 );

                stream.Seek( 15888, SeekOrigin.Begin );
                UInt32 end = (UInt32)stream.Position;
                UInt32 start = 8;

                for( int i = 1; i <= 3970; i++ )
                {
                    stream.Seek( start, SeekOrigin.Begin );
                    stream.Write( end.ToBytes(), 0, 4 );
                    start += 4;

                    stream.Seek( end, SeekOrigin.Begin );
                    
                    byte[] bytes = ReadFile( path, i );
                    if( bytes.Length != 0 )
                    {
                        stream.Write( bytes, 0, bytes.Length );
                    }

                    end = (UInt32)stream.Position;

                    worker.ReportProgress( (i * 100) / 3790 );
                }
            }
            catch( Exception )
            {
                throw;
            }
            finally
            {
                if( stream != null )
                {
                    stream.Flush();
                    stream.Close();
                    stream = null;
                }
            }
        }

